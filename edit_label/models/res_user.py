from odoo import api, fields, models


class ResUser(models.Model):
    _inherit = 'res.users'

    edit_field_label = fields.Boolean(string='Edit Field Label', default=False)

    @api.model
    def login_user(self):
        user = self.env.user
        return user.edit_field_label
